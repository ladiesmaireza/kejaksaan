<h1 class="TugasDanWewenang">TUGAS DAN WEWENANG</h1>

<p style="text-align:justify;">
    Berdasarkan Undang-Undang Republik Indonesia Nomor 11 Tahun 2021 Tentang Perubahan Atas Undang-Undang Nomor 16 Tahun 2004 Tentang Kejaksaan Republik Indonesia, Bagian Pertama pada BAB III TUGAS DAN WEWENANG yang telah diubah sehingga berbunyi “Diantara Pasal 30 dan Pasal 31 disisipkan 3 (tiga) pasal, yakni Pasal 30A, Pasal 30B, dan Pasal 30C”.
</p>

<p style="text-align:justify;">
    <strong>Pasal 30</strong><br>
    Ayat (1) Di bidang pidana, kejaksaan mempunyai tugas dan wewenang :
</p>
<ul>
    <li>Melakukan penuntutan;</li>
    <li>Melaksanakan penetapan hakim dan putusan pengadilan yang telah memperoleh kekuatan hukum tetap;</li>
    <li>Melakukan pengawasan terhadap pelaksanaan putusan pidana bersyarat, putusan pidana pengawasan, dan keputusan lepas bersyarat;</li>
    <li>Melakukan penyidikan terhadap tindak pidana tertentu berdasarkan undang-undang;</li>
    <li>Melengkapi berkas perkara tertentu dan untuk itu dapat melakukan pemeriksaan tambahan sebelum dilimpahkan ke pengadilan yang dalam pelaksanaannya dikoordinasikan dengan penyidik.</li>
</ul>

<p style="text-align:justify;">
    Ayat (2) Di bidang perdata dan tata usaha negara, kejaksaan dengan kuasa khusus dapat bertindak baik di dalam maupun di luar pengadilan untuk dan atas nama negara atau pemerintah.
</p>

<p style="text-align:justify;">
    Ayat (3) Dalam bidang ketertiban dan ketenteraman umum, kejaksaan turut menyelenggarakan kegiatan:
</p>
<ul>
    <li>Peningkatan kesadaran hukum masyarakat;</li>
    <li>Pengamanan kebijakan penegakan hukum;</li>
    <li>Pengawasan peredaran barang cetakan;</li>
    <li>Pengawasan aliran kepercayaan yang dapat membahayakan masyarakat dan negara;</li>
    <li>Pencegahan penyalahgunaan dan/atau penodaan agama;</li>
    <li>Penelitian dan pengembangan hukum serta statistik kriminal.</li>
</ul>

<p style="text-align:justify;"><strong>Pasal 30A</strong> berbunyi: Dalam pemulihan aset, Kejaksaan berwenang melakukan kegiatan penelusuran, perampasan, dan pengembalian aset perolehan tindak pidana dan aset lainnya kepada negara, korban, atau yang berhak.</p>

<p style="text-align:justify;"><strong>Pasal 30B</strong> berbunyi: Dalam Bidang Intelijen penegakan hukum, Kejaksaan berwenang menyelenggarakan fungsi penyelidikan, pengamanan, dan penggalangan untuk kepentingan penegakan hukum, melakukan kerja sama intelijen, pencegahan KKN, dan pengawasan multimedia.</p>

<p style="text-align:justify;"><strong>Pasal 30C</strong> berbunyi: Selain melaksanakan tugas dan wewenang sebagaimana dimaksud dalam Pasal 30, Pasal 30A, dan Pasal 30B Kejaksaan turut serta dalam kegiatan statistik kriminal, penanganan HAM, mediasi penal, penyadapan, serta fungsi perdata dan publik lainnya.</p>

